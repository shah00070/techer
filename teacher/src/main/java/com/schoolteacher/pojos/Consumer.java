package com.schoolteacher.pojos;

public class Consumer {
	private String MemberDetails;
	private String ConsumerProfile;
	private String ConsumerContactInfo;
	private String MemberEmergencyDetails;

	
	/**
	 * @return the memberDetails
	 */
	public String getMemberDetails() {
		return MemberDetails;
	}

	/**
	 * @param memberDetails
	 *            the memberDetails to set
	 */
	public void setMemberDetails(String memberDetails) {
		MemberDetails = memberDetails;
	}

	/**
	 * @return the consumerProfile
	 */
	public String getConsumerProfile() {
		return ConsumerProfile;
	}

	/**
	 * @param consumerProfile
	 *            the consumerProfile to set
	 */
	public void setConsumerProfile(String consumerProfile) {
		ConsumerProfile = consumerProfile;
	}

	/**
	 * @return the consumerContactInfo
	 */
	public String getConsumerContactInfo() {
		return ConsumerContactInfo;
	}

	/**
	 * @param consumerContactInfo
	 *            the consumerContactInfo to set
	 */
	public void setConsumerContactInfo(String consumerContactInfo) {
		ConsumerContactInfo = consumerContactInfo;
	}

	/**
	 * @return the memberEmergencyDetails
	 */
	public String getMemberEmergencyDetails() {
		return MemberEmergencyDetails;
	}

	/**
	 * @param memberEmergencyDetails the memberEmergencyDetails to set
	 */
	public void setMemberEmergencyDetails(String memberEmergencyDetails) {
		MemberEmergencyDetails = memberEmergencyDetails;
	}

	


}
