package com.schoolteacher.mylibrary.pojo;

public class ConsumerProfiles {
	private int ProfileId;
    private String ProfileType ;
    private String ProfileUniqueId;
	/**
	 * @return the profileId
	 */
	public int getProfileId() {
		return ProfileId;
	}
	/**
	 * @param profileId the profileId to set
	 */
	public void setProfileId(int profileId) {
		ProfileId = profileId;
	}
	/**
	 * @return the profileType
	 */
	public String getProfileType() {
		return ProfileType;
	}
	/**
	 * @param profileType the profileType to set
	 */
	public void setProfileType(String profileType) {
		ProfileType = profileType;
	}
	/**
	 * @return the profileUniqueId
	 */
	public String getProfileUniqueId() {
		return ProfileUniqueId;
	}
	/**
	 * @param profileUniqueId the profileUniqueId to set
	 */
	public void setProfileUniqueId(String profileUniqueId) {
		ProfileUniqueId = profileUniqueId;
	}
}
